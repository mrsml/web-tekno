            <p>Osmanlı hakimiyetine geçişi Orhan Gazi’nin komutanlarından Konuralp Bey'in 1323 yılında Bizans tekfurları ile yaptığı savaş neticesinde gerçekleşmiştir.
            Yörenin adı komutan Konuralp'e izafeten verilmiş ve günümüze kadar bu şekilde ulaşmıştır.Bağlı Konuralp beldesinde yer alan antik şehrin tarihi, Milattan Önce 3.y.y. kadar dayanıyor.
            Geyve, Alp Suyu, Karaceebüş hisarlarını fetheden Konuralp, Akçakoca ve Abdurrahman Gaziler, Bizans devrinde Regio Torsia adı verilen Akova’ya akınlara başladılar. 
