            beklemiş ve arkasından kesin darbeyi vurmuştur. Osman Gazi, Düzce Pazarı (ovasını) ve Bizans Prusias’ını, Konuralp’e verdi. 14.y.y. ilk çeyreğinden itibaren bu bölge Konuralp ilçesi ve kısaca “ Konrapa “ diye anılır.</p>
