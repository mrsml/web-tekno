            17 Ağustos 1999 ve 12 Kasım 1999 depremlerini yaşayan Düzce 1’i yeni 6’sı eski ilçe dahil edilerek 09 Aralık 1999 tarih ve 23091 Sayılı Bakanlar Kurulu kararıyla 
            Resmi Gazetede yayınlanan “ Bir İl ve iki ilçe kurulması ve 190 sayılı Kanun Hükmünde Kararnamenin eki Cetvellerde Değişiklik Yapılması Hakkında 
