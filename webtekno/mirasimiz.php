          göre su kemerlerinin yapımı için, M.S. 3. yüzyılda kentin
          aristokratlarından Gavinius Sacerdos yardımda bulunmuş. M.S. 2.
          yüzyılın başlarında ise P. Domitius Iulianus kente su getirilmesi için
          maddi destek sağlamış.
        </p>
