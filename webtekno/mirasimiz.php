        <h2>Konuralp Müzesi</h2>
        <p>
          Antik Roma kenti olan “Prusias Ad Hypium” üzerine kurulu olan
          Konuralp’de, antik döneme ait eserlerin korunmasını sağlamak ve
          sergilemek amacıyla yapımına başlanan Konuralp Müzesi, 1993 yılında
          ziyarete açıldı.
        </p>
        <p>
          Konuralp Müzesi’nde 1825 adet arkeolojik 456 adet etnografik ve 3837
          adet sikke olmak üzere, toplam 6118 adet eser bulunuyor. Bu eserler
          müze bahçesi, arkeoloji, etnografya ve taş eserler salonları ile sikke
          bölümünde sergileniyor. Müze bahçesinde, tamamı Konuralp’te çıkarılan
          ve Roma Dönemine ait mimari parçalar, mezar stelleri, lahit, sütun ve
          sütun başlıkları bulunuyor. “Arkeoloji Salonu”ndaki eserler ise
          Neolitik, Eski Tunç, Helenistik ve Roma dönemlerine ait. Bunlar
          arasında pişmiş toprak, bronz ve cam eserlerin değişik fonksiyon ve
          türdeki örnekleri yer alıyor. Aynı salonda Konuralp yakınlarındaki
          Çavuşlar Köyü’nde ele geçirilen mezar buluntuları da görülebilir.
        </p>
        <p>
          Müzenin Etnografya Salonu’nda yakın geçmişimize ait 19. ve 20. yüzyıl
          Osmanlı dönemine ait kıyafet, ev eşyası ve silahlardan oluşan eserler
          sergileniyor. Taş Eserler Salonu’nda ise Prusias kentine ait olan ve
          Konuralp’te çıkarılmış mermerden çeşitli heykel ve mimari parçalar
          bulunuyor. Sikke Bölümü’nde de Grek şehirleri, Roma imparatorları ve
          Osmanlı padişahlarına ait altın, gümüş ve bronz sikkeler, kronolojik
          olarak sergileniyor.
        </p>
